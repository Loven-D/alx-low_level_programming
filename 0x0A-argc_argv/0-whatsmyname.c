#include <stdio.h>
#include <stdlib.h>

/**
 * main - is the entry point.
 * @argc: variables that contains the number of arguments passed
 * @argv: an arrays of pointers to the argument
 * Return: 0 always.
 */
int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
